import Foundation

class DataEntry {
    var hoogvogels: Int
    var zijvogels: Int
    var kalleVogels: Int
    var kleineVogels: Int
    
    init(hoogvogels: Int, zijvogels: Int, kalleVogels: Int, kleineVogels: Int){
        self.hoogvogels = hoogvogels
        self.zijvogels = zijvogels
        self.kalleVogels = kalleVogels
        self.kleineVogels = kleineVogels
    }
}
